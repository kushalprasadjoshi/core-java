package chap02operators;

public class Eg04RelationalOperators {
    public static void main(String[] args) {
        int num1 = 5, num2 = 6;

        System.out.println(num1 == num2); // false
        System.out.println(num1 != num2); // true
        System.out.println(num1 > num2);  // false
        System.out.println(num1 < num2); // true
        System.out.println(num1 >= num2); // false
        System.out.println(num1 <= num2); // true
    }
}
