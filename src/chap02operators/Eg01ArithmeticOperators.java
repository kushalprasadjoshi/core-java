package chap02operators;

public class Eg01ArithmeticOperators {
    public static void main(String[] args) {
        int a = 5, b = 3;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
        System.out.println("++a = " + ( ++ a));
        System.out.println("--b = " + (--b));
    }
}
